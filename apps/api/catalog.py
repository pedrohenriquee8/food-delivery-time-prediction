def get_restaurant_by_id(restaurant_id: str) -> dict | None:
    return RESTAURANTS_BY_ID.get(restaurant_id)


def get_restaurant_sections(category_id: str | None = None) -> list[dict]:
    if not category_id:
        return RESTAURANT_SECTIONS

    filtered = []
    for section in RESTAURANT_SECTIONS:
        restaurants = [
            restaurant
            for restaurant in section["restaurants"]
            if category_id in restaurant["categoryIds"]
        ]
        if restaurants:
            filtered.append({**section, "restaurants": restaurants})

    return filtered
